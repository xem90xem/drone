package com.drive.drone.model;

/**
 * The Class Urbanization.
 */
public class Urbanization {
    
    /** The id. */
    private String id;
    
    /** The coord X. */
    private String coordX;
    
    /** The coord Y. */
    private String coordY;
    
    /**
     * Gets the id.
     *
     * @return the id
     */
    public String getId(){
        return id;
    }
    
    /**
     * Sets the id.
     *
     * @param id the new id
     */
    public void setId(
        String id){
        this.id = id;
    }
    
    /**
     * Gets the coord X.
     *
     * @return the coord X
     */
    public String getCoordX(){
        return coordX;
    }
    
    /**
     * Sets the coord X.
     *
     * @param coordX the new coord X
     */
    public void setCoordX(
        String coordX){
        this.coordX = coordX;
    }
    
    /**
     * Gets the coord Y.
     *
     * @return the coord Y
     */
    public String getCoordY(){
        return coordY;
    }
    
    /**
     * Sets the coord Y.
     *
     * @param coordY the new coord Y
     */
    public void setCoordY(
        String coordY){
        this.coordY = coordY;
    }
    
}
